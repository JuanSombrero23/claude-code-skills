<#
================================================================================
  Apply-SessionRenames.ps1
================================================================================

  WHAT THIS IS
    Batch-applies custom titles to Claude Code session transcripts that still
    carry the auto-generated default. The skill's `rename` mode produces a
    proposals JSON listing sessions and their proposed customer-topic-action
    slugs; this script consumes that JSON and (optionally) writes the new
    titles into the .jsonl files.

  HOW THE RENAME WORKS ON DISK
    `/rename <name>` appends an undocumented marker line to the session
    transcript:
        {"type":"custom-title","customTitle":"<name>","sessionId":"<id>"}
        {"type":"agent-name","agentName":"<name>","sessionId":"<id>"}
    Confirmed empirically against session 51f02ff0-... in May 2026. The picker
    reads the LAST such line. This script appends the same two lines.

  SAFETY
    - Dry-run is the default. `-Apply` is required to actually write.
    - Every file is backed up to ~/.claude/projects-backup/<UTC-date>/ before
      mutation; the backup path is logged.
    - Refuses to touch any .jsonl modified within the last 10 minutes (live
      session protection). Override with `-AllowRecent` only if you really
      know the session is closed.
    - Skips sessions that already have a custom title unless `-Force`.

  PROPOSALS JSON FORMAT (input)
    {
      "proposals": [
        { "id": "07092173-...", "file": "C:\\...\\07092173-....jsonl",
          "newTitle": "acme-reporting-debugging-prompts" }
      ]
    }

  USAGE
    .\Apply-SessionRenames.ps1 -ProposalsFile rename-proposals.json
    .\Apply-SessionRenames.ps1 -ProposalsFile rename-proposals.json -Apply
    .\Apply-SessionRenames.ps1 -ProposalsFile rename-proposals.json -Apply -AllowRecent

  CREATED
    2026-05-30. Part of session-curator v1. See plan §C.2/§H.1.
================================================================================
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory=$true)]
    [string] $ProposalsFile,
    [switch] $Apply,
    [switch] $AllowRecent,
    [switch] $Force,
    [string] $BackupRoot = (Join-Path $HOME ".claude\projects-backup\$(Get-Date -Format 'yyyy-MM-dd')")
)

$ErrorActionPreference = 'Stop'

if (-not (Test-Path $ProposalsFile)) {
    throw "Proposals file not found: $ProposalsFile"
}

$parsed = Get-Content $ProposalsFile -Raw | ConvertFrom-Json
# Accept either shape: bare array of proposals, or {proposals: [...]} wrapper.
# Smoke test v2 caught the LLM emitting bare arrays after re-reading from another
# subagent's output; tolerating both shapes is cheaper than re-teaching every caller.
$proposals = if ($parsed -is [System.Collections.IList]) { $parsed } else { $parsed.proposals }
if (-not $proposals) { throw "Proposals file is empty or has no .proposals array" }

Write-Host "Loaded $($proposals.Count) proposal(s) from $ProposalsFile" -ForegroundColor Cyan
if (-not $Apply) {
    Write-Host "[DRY RUN] Pass -Apply to actually write." -ForegroundColor Yellow
}

$results = New-Object 'System.Collections.Generic.List[object]'
$now = Get-Date
foreach ($p in $proposals) {
    if (-not (Test-Path $p.file)) {
        $results.Add([pscustomobject]@{ id=$p.id; status='missing'; reason='file not found' }) | Out-Null
        continue
    }
    $file = Get-Item $p.file
    $ageMin = ($now - $file.LastWriteTime).TotalMinutes
    if ($ageMin -lt 10 -and -not $AllowRecent) {
        $results.Add([pscustomobject]@{ id=$p.id; status='skipped'; reason="modified $([int]$ageMin) min ago (live session protection)" }) | Out-Null
        continue
    }

    # Check for existing custom title
    $hasCustom = Select-String -Path $file.FullName -Pattern '"type":"custom-title"' -Quiet
    if ($hasCustom -and -not $Force) {
        $results.Add([pscustomobject]@{ id=$p.id; status='skipped'; reason='already has custom title (-Force to overwrite)' }) | Out-Null
        continue
    }

    if (-not $Apply) {
        $results.Add([pscustomobject]@{ id=$p.id; status='would-rename'; newTitle=$p.newTitle; file=$file.FullName }) | Out-Null
        continue
    }

    # Backup — create folder and write the provenance README if not present yet
    if (-not (Test-Path $BackupRoot)) { New-Item -ItemType Directory -Path $BackupRoot -Force | Out-Null }
    $readmePath = Join-Path $BackupRoot '_README.md'
    if (-not (Test-Path $readmePath)) {
        $readme = @"
# Claude session backups — $(Split-Path $BackupRoot -Leaf)

**What this folder contains:** byte-identical copies of Claude Code session ``.jsonl`` files taken IMMEDIATELY before the session-curator skill appended a ``custom-title`` rename marker to them. One file per renamed session, named ``<sessionId>__<HHMMSS>.jsonl``.

**Generated by:** ``session-curator/scripts/Apply-SessionRenames.ps1`` on $(Get-Date -Format 'yyyy-MM-dd') (UTC dates used for folder names).

**Why backups exist:** the rename mechanism appends a line of an UNDOCUMENTED format (``{"type":"custom-title",...}``) to live session transcripts. If Claude Code's transcript reader ever rejects this format, or if you want to undo a rename, the backup lets us restore the exact pre-mutation state.

**How to restore one session:** copy the backup back over the live file (close any Claude session writing to that file first):
``````powershell
Copy-Item "<this-folder>\<sessionId>__<HHMMSS>.jsonl" ``
          "~/.claude/projects/<encoded-project>/<sessionId>.jsonl" -Force
``````

**Auto-cleanup:** folders older than 30 days are pruned automatically by ``Apply-SessionRenames.ps1`` on every ``-Apply`` run (mirrors Claude's own ``cleanupPeriodDays`` default).

**Safe to delete:** Yes — if you no longer need to undo any renames in this folder. Deleting it has no effect on your live sessions.
"@
        Set-Content -Path $readmePath -Value $readme -Encoding UTF8
    }
    $backupName = "$($file.BaseName)__$(Get-Date -Format 'HHmmss').jsonl"
    $backupPath = Join-Path $BackupRoot $backupName
    Copy-Item $file.FullName $backupPath

    # Append the two marker lines
    $titleLine = '{"type":"custom-title","customTitle":"' + ($p.newTitle -replace '\\','\\\\' -replace '"','\"') + '","sessionId":"' + $p.id + '"}'
    $agentLine = '{"type":"agent-name","agentName":"' + ($p.newTitle -replace '\\','\\\\' -replace '"','\"') + '","sessionId":"' + $p.id + '"}'

    # Ensure existing file ends with newline before appending
    $tail = [System.IO.File]::ReadAllText($file.FullName)
    if ($tail.Length -gt 0 -and $tail[-1] -ne "`n") {
        Add-Content -Path $file.FullName -Value "" -NoNewline
    }
    Add-Content -Path $file.FullName -Value $titleLine
    Add-Content -Path $file.FullName -Value $agentLine

    $results.Add([pscustomobject]@{
        id        = $p.id
        status    = 'renamed'
        newTitle  = $p.newTitle
        file      = $file.FullName
        backup    = $backupPath
    }) | Out-Null
}

Write-Host ""
$results | Format-Table -AutoSize

$summary = $results | Group-Object status | Select-Object Name, Count
Write-Host ""
Write-Host "Summary:" -ForegroundColor Cyan
$summary | Format-Table -AutoSize

if ($Apply -and ($results | Where-Object { $_.status -eq 'renamed' }).Count -gt 0) {
    Write-Host "Backups saved to: $BackupRoot" -ForegroundColor Green
}

# Prune backup folders older than 30 days (mirrors Claude's cleanupPeriodDays default)
if ($Apply) {
    $backupParent = Split-Path $BackupRoot
    if (Test-Path $backupParent) {
        $cutoff = (Get-Date).AddDays(-30)
        $oldFolders = Get-ChildItem $backupParent -Directory | Where-Object {
            $_.Name -match '^\d{4}-\d{2}-\d{2}$' -and $_.LastWriteTime -lt $cutoff
        }
        if ($oldFolders) {
            Write-Host "Pruning $($oldFolders.Count) backup folder(s) older than 30 days..." -ForegroundColor DarkGray
            $oldFolders | Remove-Item -Recurse -Force
        }
    }
}
